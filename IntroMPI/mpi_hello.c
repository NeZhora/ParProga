/* mpi_hello.c
 * 
 * Каждый MPI-процесс выводит "Hello World", свой ранг и общее число процессов.
 *
 * Компиляция: mpicc -o mpi_hello mpi_hello.c
 * Запуск:     mpirun -n 4 ./mpi_hello
 */


#include <stdio.h>
#include <mpi.h>

int main(int argc, char *argv[])
{
    int rank;  /* Ранг (номер) текущего процесса */
    int size;  /* Общее число процессов в коммуникаторе */

    /* Инициализация среды MPI. Обязательно вызывается первой из MPI-функций.
     * Передаём argc и argv, чтобы MPI мог обработать свои аргументы командной строки. */
    MPI_Init(&argc, &argv);

    /* Получаем ранг текущего процесса в коммуникаторе MPI_COMM_WORLD.
     * MPI_COMM_WORLD — коммуникатор по умолчанию, включающий все запущенные процессы. */
    MPI_Comm_rank(MPI_COMM_WORLD, &rank);

    /* Получаем общее число процессов в коммуникаторе */
    MPI_Comm_size(MPI_COMM_WORLD, &size);

    /* Каждый процесс выводит приветствие со своим рангом и общим числом процессов.
     * Порядок вывода НЕ гарантирован — процессы работают асинхронно,
     * поэтому строки могут появляться в произвольном порядке. */
    printf("Hello World from process %d of %d\n", rank, size);

    /* Завершение работы среды MPI. После этого вызова нельзя использовать MPI-функции. */
    MPI_Finalize();

    return 0;
}